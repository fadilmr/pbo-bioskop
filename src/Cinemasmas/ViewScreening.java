    public void getScreening() {
        ResultSet rs;
        String query = "SELECT * FROM screening";
        
        try {
            rs = Config.AccessDB(query);
            int i = 0;
            while(rs.next()) {
                ScreeningList1.add(rs.getString("MovieTitle") + " (" + rs.getString("TheaterNum") + ")");       
                Ids[i] = rs.getString("Screening_ID");
                i++;
            }
            Config.disconnect();
        } catch (Exception e) {
            e.printStackTrace();
        }
        

    }
   
    public void getMovieTitle() {
        String query = "SELECT * FROM movie";
        try {
            ResultSet rs = Config.AccessDB(query);
            while(rs.next()) {
                MovieBox.addItem(rs.getString("Title"));
            } 
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
