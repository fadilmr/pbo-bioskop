    private void AddButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButton1ActionPerformed
        // TODO add your handling code here:
        String title = MovieBox.getSelectedItem().toString();
        String theater = TheaterBox.getSelectedItem().toString();
        String jam = JamField.getText();
        String price = PriceField.getText();
        
        try {
            t.addScreening1(title, theater, jam, price);
            JOptionPane.showMessageDialog(this, "Screening berhasil ditambahkan");
            RefreshList();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR");
        }
        
    }//GEN-LAST:event_AddButton1ActionPerformed

    private void EditButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditButton1ActionPerformed
        // TODO add your handling code here:
        String title = MovieBox.getSelectedItem().toString();
        String theater = TheaterBox.getSelectedItem().toString();
        String jam = JamField.getText();
        String price = PriceField.getText();
        
        try {
            t.EditScreening(Ids[ScreeningList1.getSelectedIndex()], title, theater, jam, price);
            JOptionPane.showMessageDialog(this, "Screening berhasil diubah");
            RefreshList();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR");
        }
    }//GEN-LAST:event_EditButton1ActionPerformed

    private void DeleteButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButton1ActionPerformed
        // TODO add your handling code here:
        try {
            t.DeleteScreening(Ids[ScreeningList1.getSelectedIndex()]);
            JOptionPane.showMessageDialog(this, "Screening berhasil dihapus");
            RefreshList();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR");
        }
    }//GEN-LAST:event_DeleteButton1ActionPerformed

    private void ScreeningList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ScreeningList1MouseClicked
        // TODO add your handling code here:
        String query = "SELECT * FROM `screening` WHERE `Screening_ID`='"+ Ids[ScreeningList1.getSelectedIndex()] +"'";
        try {
            ResultSet rs = Config.AccessDB(query);
            while(rs.next()) {
                MovieBox.setSelectedItem(rs.getString("MovieTitle"));
                TheaterBox.setSelectedItem(rs.getString("TheaterNum"));
                JamField.setText(rs.getString("JamTayang"));
                PriceField.setText(rs.getString("Price"));
            }
            Config.disconnect();
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_ScreeningList1MouseClicked

    
    public void RefreshList() {
        ScreeningList1.removeAll();
        getScreening();
    }
